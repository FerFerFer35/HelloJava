package com.lfernando.basic;

public class Variables {
    public static void main(String[] args) {
        //In Java there are different types of variables, for example:

        String myString = "Hello World"; //Stores a text
        int myInt = 1; //Stores integers (numbers) without decimal
        float myFloat = 1.1f; //Stores a numbers with decimals
        char myChar = 'a'; //Stores a single character
        double myDouble = 1.2; //Similar to float, stores numbers with decimal
        boolean myBoolean = true; //Stores values with two states: true or false

        int myNum = 5;               // Integer (whole number)
        float myFloatNum = 5.99f;    // Floating point number
        char myLetter = 'D';         // Character
        boolean myBool = true;       // Boolean
        String myText = "Hello";     // String
    }
}
