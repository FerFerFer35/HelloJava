package com.lfernando.basic; /* In this case a package is basically folders inside more folders,
                                in this case the first folder name is "com"*/

//Every Java program has a class name, this class name must match with the filename.
public class HelloWorld {
    public static void main(String[] args) {
        //Any code inside the main method will be executed, in this case the next line of code ⬇️
        System.out.println("Hello World");
        //The previous line of code print in console the sentence "Hello World". Run it and try.

        //The method "print" used to print in the same line.
        System.out.print("Hello");
        System.out.print("World");
    }
}
